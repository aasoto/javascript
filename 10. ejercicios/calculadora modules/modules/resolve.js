export function add(x=0, y=0) {
  return x + y
}

export function subtract(x=0, y=0) {
  return x - y
}

export function multiply(x=1, y=1) {
  return x * y
}

export function divide(x=1, y=1) {
  return x / y
}

export function percent(x=1, y=100) {
  return (x * y)/100
}

export function expo(x=1, y=1) {
  return x ** y
}